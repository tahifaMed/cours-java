public class Exercise02 {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("x initial : " + x);
        
        x += 5;
        System.out.println("Après += 5 : " + x);
        
        x -= 3;
        System.out.println("Après -= 3 : " + x);
        
        x *= 2;
        System.out.println("Après *= 2 : " + x);
        
        x /= 4;
        System.out.println("Après /= 4 : " + x);
        
        x %= 3;
        System.out.println("Après %= 3 : " + x);
    }
}
